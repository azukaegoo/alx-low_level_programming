#include "main.h"
/**
 * _abs - computes the absolute value of the integer argument
 * int _abs(int) - computing the absolute value of an integer
 * @n: is the parameter name of the integer argument
 * Return: -n for less than 0 and n for greater than 0
*/
int _abs(int n)
{

	if (n < 0)
	{
	return (-n);
	}
	else
	{
	return (n);
	}
}
